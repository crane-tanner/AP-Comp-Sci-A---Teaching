public class Dog {
        public int age;
        public String breed;
        public String name;

    public Dog(int newAge, String  newBreed, String newName) {
            age = newAge;
            breed = newBreed;
            name = newName;
        }

    public Dog() {
            age = 0;
            breed = "N/A";
            name = "None";
        }
    }
